# Write a Python program to find the maximum and minimum numbers from the specified decimal numbers.

# declaring a list of decimal
mylist = [1.2,3.3,0.1,4.7,8.2,9.1]

# using Max and Min function 
print("The Maximum Decimal from the List is : ",max(mylist))
print("The Minimum Decimal from the List is : ",min(mylist))
