# Write a Python program to find the second smallest number in a list.

# creating a list
mylist = [13,24,3,47,15,10,78,81,9]

# sorting the list with sort() function
mylist.sort()

# printing the sorted list
print(mylist)

# printing the second number from the list as it is sorted list so it will be second smallest number it self
print("The Second Smallest Number : ",mylist[1])